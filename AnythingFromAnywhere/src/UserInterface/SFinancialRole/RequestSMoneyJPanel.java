    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        if(txtAmount.getText().equals("")||Float.parseFloat(txtAmount.getText())<0){
            JOptionPane.showMessageDialog(null, "Money request is invalid");
        }
        float money = Float.parseFloat(txtAmount.getText());
        if(money > enterprise.getTotalProfit()){
                 JOptionPane.showMessageDialog(null, "Your total profit is not enough!");
                 return;
            }
            else{
                
                MoneyOrder mo = moneyOrderDirectory.createMoneyOrder();
                mo.setTotalProfit(enterprise.getTotalProfit());
                mo.setMoney(Float.parseFloat(txtAmount.getText()));
                mo.setMoneyOrderStatus("Processing");
                mo.setName(enterprise.getName());
                //enterprise.setTotalProfit(enterprise.getTotalProfit()- Float.parseFloat(txtAmount.getText()));

    		
                JOptionPane.showMessageDialog(null, "Request has been submitted Successfully");}
        txtAmount.setText("");
        
        logr.info("Store Money Request Button clicked");
    }//GEN-LAST:event_btnConfirmActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
        Component [] comps = userProcessContainer.getComponents();
        for(Component c : comps){
            if(c instanceof SFinancialWorkAreaJPanel){
                SFinancialWorkAreaJPanel panel = (SFinancialWorkAreaJPanel) c;
                panel.populateMoneyOrderJTable();
            }
        }
    }//GEN-LAST:event_btnBackActionPerformed


