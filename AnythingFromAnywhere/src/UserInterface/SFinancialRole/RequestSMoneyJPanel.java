    void calculateTotalProfit(){
        float sum = 0;
        for(Order o : system.getOrderDirectory().getOrderList()){
            if(o.getSname().equals(enterprise.getName())&&o.getStatus().equals("Finished")){
                sum += o.getTotalPrice();
            }
        }
        sum=(float) (sum*0.9);
        for(MoneyOrder m :moneyOrderDirectory.getMoneyOrderList()){
            if(m.getName().equals(enterprise.getName())&&m.getMoneyOrderStatus().equals("Approved")){
               sum-=m.getMoney();
        }}
        enterprise.setTotalProfit(sum);
    }
