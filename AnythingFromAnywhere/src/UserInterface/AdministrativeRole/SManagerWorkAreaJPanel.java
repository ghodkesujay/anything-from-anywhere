    private void populateOrderTable(){
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        for (Order o : system.getOrderDirectory().getOrderList()){
            if(o.getStatus().equals("Ordered")&&o.getSname().equals(enterprise.getName())){
                Object[] row = new Object[5];
                row[0] = o;
                row[1] = o.getTotalPrice();
                row[2] = o.getSname();
                row[3] = o.getDname();
                row[4] = o.getStatus();
                model.addRow(row);
            
            
        }
    }
    }
    void populatemodifyMenuJTable(){
        DefaultTableModel model = (DefaultTableModel) menuJTable.getModel();
        model.setRowCount(0);
        for (ModifyItemsRequest m : enterprise.getModifyDishesRequestDirectory().getModifyItmesRequestList()){
            if(m.getModifyStatus().equals("Processing")){
            Object[] row = new Object[5];
            row[0] = m.getItem().getItemName();
            row[1] = m.getOriginalPrice();
            row[2] = m.getFinalPrice();
            row[3] = m;
            row[4] = m.getModifyStatus();
            model.addRow(row);
            }
        }
    }
    
    void populatePurchaseTable(){
        DefaultTableModel model = (DefaultTableModel) purchaseJTable.getModel();
        model.setRowCount(0);
        for (PurchaseRequest p : enterprise.getPurchaseRequestDirectory().getPurchaseRequestList()){
            if(p.getPurchaseRequestStatus().equals("Processing")){
                Object[] row = new Object[4];
                row[0] = p.getMaterialName();
                row[1] = p.getMoneyOrdered();
                row[2] = p;
                row[3] = p.getPurchaseRequestStatus();
                model.addRow(row);
            }
            
        }
    }
