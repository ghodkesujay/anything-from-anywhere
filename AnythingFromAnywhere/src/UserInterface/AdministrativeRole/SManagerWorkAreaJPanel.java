    private void btnPApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPApproveActionPerformed
        // TODO add your handling code here:
        int selectedrow = purchaseJTable.getSelectedRow();

        if(selectedrow >=0)
        {
            PurchaseRequest  vs = (PurchaseRequest)purchaseJTable.getValueAt(selectedrow,2);
            
            vs.setPurchaseRequestStatus("Approved By Manager");
            
            
            
            JOptionPane.showMessageDialog(null, "This request has been approved!!");
            populatePurchaseTable();
        }
        else
        JOptionPane.showMessageDialog(null,"Please select an request!! ");
    }//GEN-LAST:event_btnPApproveActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedrow = jTable2.getSelectedRow();

        if(selectedrow >=0)
        {
            Order  vs = (Order)jTable2.getValueAt(selectedrow,0);
            
            vs.setStatus("Prepared");
            
            
            JOptionPane.showMessageDialog(null, "This order has been prepared");
            populateOrderTable();
        }
        else
        JOptionPane.showMessageDialog(null,"Please select an order to manage ");
    }//GEN-LAST:event_jButton2ActionPerformed

    private void btnMApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMApproveActionPerformed
        // TODO add your handling code here:
        int selectedrow = menuJTable.getSelectedRow();

        if(selectedrow >=0)
        {
            ModifyItemsRequest  vs = (ModifyItemsRequest)menuJTable.getValueAt(selectedrow,3);
            
            vs.setModifyStatus("Approved");
            
            vs.getItem().setPrice(vs.getFinalPrice());
            
            
            JOptionPane.showMessageDialog(null, "This request has been approved!!");
            populatemodifyMenuJTable();
        }
        else
        JOptionPane.showMessageDialog(null,"Please select an request!! ");
    }//GEN-LAST:event_btnMApproveActionPerformed

